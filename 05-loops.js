// LOOPS

// For loop
for (let i = 0; i < 5; i++) {
  console.log("For loop number:", i);
}

// While loop
let count = 0;
while (count < 3) {
  console.log("While loop count:", count);
  count++;
}

// For-of loop
const fruits = ["apple", "banana", "cherry"];
for (const fruit of fruits) {
  console.log("Fruit:", fruit);
}
