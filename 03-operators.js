// OPERATORS

// Arithmetic
let x = 10;
let y = 3;
console.log(x + y); // 13
console.log(x - y); // 7
console.log(x * y); // 30
console.log(x / y); // 3.33
console.log(x % y); // 1

// Comparison
console.log(x > y);  // true
console.log(x === 10); // true
console.log(x !== y);  // true

// Logical
console.log(true && false); // false
console.log(true || false); // true
console.log(!true);         // false
