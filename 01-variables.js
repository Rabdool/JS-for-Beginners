// VARIABLES

// 'let' allows you to declare a variable that can be changed
let name = "Alice";
console.log(name); // Output: Alice

// 'const' declares a constant (cannot be reassigned)
const age = 25;
console.log(age); // Output: 25

// 'var' is the old way to declare variables (avoid it in modern JS)
var city = "New York";
console.log(city); // Output: New York
